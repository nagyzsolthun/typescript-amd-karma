requirejs.config({
    deps: Object.keys(window.__karma__.files).filter(isTestFile)
    ,baseUrl: "/base/js" // karma refers to files with the "base" prefix
    ,callback: window.__karma__.start
});

// filesNames ending to .spec.js are considered test files
function isTestFile(fileName) {
  var testFileRegex = /spec[.]js$/;
  return testFileRegex.test(fileName);
}
